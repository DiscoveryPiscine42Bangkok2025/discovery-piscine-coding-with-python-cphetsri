#!/usr/bin/env python3

def main():
    stnum = int(input("Give me the first number: "))
    sdnum = int(input("Give me the second number: "))
    print("Thank you!")
    print(f"{stnum} + {sdnum} = {stnum + sdnum} ")
    print(f"{stnum} - {sdnum} = {stnum - sdnum} ")
    print(f"{stnum} / {sdnum} = {int(stnum / sdnum)} ")
    print(f"{stnum} * {sdnum} = {stnum * sdnum} ")
main()